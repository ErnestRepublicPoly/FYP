package sg.edu.rp.c346.round3;

public class InfoClass {
    private String infoName;
    private int image;

    public InfoClass(String infoName, int image) {
        this.infoName = infoName;
        this.image = image;
    }

    public String getInfoName() {
        return infoName;
    }

    public int getImage() {
        return image;
    }
}
